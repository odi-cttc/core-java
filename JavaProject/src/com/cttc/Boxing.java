package com.cttc;

public class Boxing {

	public static void main(String[] args) {
		String a = "10";
		String b = "20";
		int i = Integer.parseInt(a);
		int j = Integer.parseInt(b);
		int c = i + j;
		System.out.println(c);
		
		String r = Integer.toString(c);
		System.out.println(r);
	}
}
